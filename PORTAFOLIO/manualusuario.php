        <h1>segunda parte</h1>
    <p>
        Piensa bien la distribución. Una vez que hayas elegido las fuentes del manual de usuario, deberás decidir dónde colocas cada cosa en las páginas.</p>
    <li>
        <ul>
            Por lo general, es conveniente poner el título del manual o del capítulo en el encabezado o en el pie de página colocando el título del manual en la página izquierda y el título del capítulo en la página derecha. También es conveniente colocar el número de página en el encabezado o en el pie de página, ya sea en el lado externo (del encabezado o del pie de página) o en el centro (únicamente en el pie de página). Es conveniente diferenciar la primera página de cada sección o capítulo de las otras páginas colocando su número de página en el centro del pie de página y en las páginas siguientes, ponerlo en la esquina exterior del encabezado.
        </ul>
    </li>
    <li>
        <ul>
            Es conveniente colocar el texto de las referencias de las imágenes en colores o en recuadros sombreados para separarlo del resto del texto. Asegúrate de elegir un color o un nivel de sombreado que permita leer el texto con facilidad.
        </ul>
    </li>
    <li>
        <ul>
            Deja unos márgenes razonablemente amplios a los lados, con un espacio extra en los bordes en los cuales vayas a unir todas las páginas.
        </ul>
    </li>
    <img src="imagenes/i11.jpg" alt="">
    <p>
        Elige el tipo de encuadernación para el manual de usuario. Si el manual de usuario tiene más de 4 páginas, será necesario unir las páginas de algún modo. Aunque los documentos de uso interno pueden engraparse en las esquinas, los manuales de usuario de uso externo que se entregan con un producto pueden encuadernarse en una de estas 3 formas:</p>
    <li>
        <ul>Engrapar a los lados resulta apropiado para los manuales compuestos por hojas de papel dobladas de 21 x 27,5 cm (8,5 x 11 pulgadas), 21 x 35 cm (8,5 x 14 pulgadas) o 27,5 x 42,5 cm (11 x 17 pulgadas). La mayoría de los manuales baratos de 48 páginas o menos se encuadernan de este modo.</ul>
    </li>
    <li>
        <ul>
            Es más común utilizar el cosido a caballo para las guías de consulta para terceros que para los manuales de usuario que se entregan con los productos que no sean automóviles, aunque algunas guías de usuario más largas se encuadernan de este modo (inicialmente, cuando JASC software producía el Paint Shop Pro, entregaban manuales de usuario encuadernados mediante el cosido a caballo).
        </ul>
    </li>
    <li>
        <ul>
            La encuadernación con espirales es más adecuada para las guías de usuario que estén diseñadas para entornos más hostiles como en exteriores, en donde los manuales encuadernados con grapas o cosidos a caballo se desarmarían. Algunos manuales encuadernados con espirales también pueden tener páginas laminadas para evitar que se arruine el manual si se moja o se ensucia.
        </ul>
    </li>
    <img src="imagenes/i12.jpg" alt="">
    <p>
        Diseña una plantilla de documento para tu manual de usuario. Muchos procesadores de texto y programas de autoedición ofrecen la posibilidad de crear plantillas de documentos para los manuales de usuario, así que a medida que escribas, el texto se mostrará automáticamente en la fuente que hayas elegido para la parte del manual en la que estés trabajando (de hecho, este artículo fue escrito inicialmente utilizando una plantilla de Microsoft Word). La mayoría de estos programas también incluyen un conjunto de plantillas predefinidas que puedes modificar según lo necesites, en vez de crear la plantilla desde el comienzo.</p>
    <li>
        <ul>
            Los procesadores de texto y los programas de autoedición también ofrecen la posibilidad de crear “estilos”, que son tipos y tamaños de fuente predefinidos para los títulos, los pies de páginas, los encabezados y el cuerpo del texto. Puedes elegir uno de los nombres de estilo predefinidos (por ejemplo, “Encabezado1”, “Normal”, “Cita”) o crea tu propio estilo con su propio nombre. Si tienes muchos estilos para la misma clase de texto, sigue las convenciones para los nombres de estilo tanto como sea posible (por ejemplo, Microsoft Word llama a los estilos de texto para los títulos como “Título1”, “Título2”, etc., para todos los títulos desde el principal hasta varios niveles de subtítulos). Intenta crear anticipadamente todos los estilos que puedas necesitar, así no tendrás que detenerte para crearlos mientras estés escribiendo realmente el manual.
        </ul>
    </li>
