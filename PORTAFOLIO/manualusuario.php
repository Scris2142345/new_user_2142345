    <h1>
        Segundo paso 
    </h1>
    <p>Escribe y dale un formato a la guía de modo tal que sea comprensible para todos los usuarios. A menos que el usuario tenga antecedentes técnicos, es posible que sea mejor evitar el lenguaje muy técnico para lograr explicaciones más claras y simples. El texto debe estar organizado de manera similar a como piensa el usuario; a menudo, tiene más sentido enumerar las características del producto agrupándolas por sus funciones que señalar solo aquellas que se utilizan más seguido.</p>
    <li>
        <ul>
            A veces es inevitable utilizar términos técnicos, como para un programa de computación destinado a crear gráficos que incluye gráficas de Fibonacci además de los gráficos de barras y circulares, que son más comunes. En ese caso, es útil definir el término y proporcionar algo de información básica al respecto, como explicar qué es un gráfico de Fibonacci y para qué se usa en el análisis financiero.
        </ul>
    </li>
    <img src="imagenes/i2.jpg" alt="">
    <h1>Tercer paso</h1>
    <p>Explica a detalle el problema que el usuario intenta resolver y luego presenta una solución para dicho problema. Ofrecer una característica del producto como una solución a un problema común funciona bien cuando se promociona el producto, pero una vez que el usuario lo tenga, debe saber cómo utilizar dicha característica. Identifica los problemas específicos que el usuario enfrentará, menciónalos en la guía de usuario y luego añade las instrucciones para resolverlos.</p>
    <li>
        <ul>
            Si el problema es complejo, sepáralo en partes más pequeñas. Enumera cada una de las partes con las instrucciones para resolverlo o lidiar con él y luego agrega sucesivamente cada parte siguiente. Dividir la información de esta manera se denomina “fragmentar”.
        </ul>
    </li>
    <img src="imagenes/i3.jpg" alt="">

    <h2>Segunda Parte</h2>
    <h1>Organizar los componentes del manual de usuario</h1>
    
    <img src="imagenes/i4.jpg" alt="">
    <p>
        Incluye la portada y las carátulas adecuadas. Deberás incluir una portada para cualquier guía de usuario mayor a una hoja de referencia así como una carátula para cualquier manual que cubra más que la superficie de una hoja de papel doblada (4 o más páginas de largo).</p>
        <li>
            <ul>
                Si el manual tiene la protección de los derechos de autor, se debe incluir un aviso de derechos de autor en la portada y en la carátula.
            </ul>
        </li>
        <li>
            <ul>
                Si existen términos y condiciones para usar el manual y el producto asociado a este, colócalos en la portada interior.
            </ul>
        </li>
        <img src="imagenes/i5.jpg" alt="">
    <h1>Segunda parte </h1>
    <p>
        Pon referencias a las documentaciones relacionadas en el prefacio. Si la documentación de usuario es más que un solo manual, coloca aquí las referencias a los demás documentos, con sus números de versiones correspondientes. El prefacio también es el lugar en donde deberás colocar (si la hubiera) una sección denominada “Cómo utilizar esta guía”.
    </p>
    <img src="imagenes/i6.jpg" alt="">
        <h1>Si el manual excede las 10 páginas, incluye un índice de contenidos.</h1>
        <img src="imagenes/i7.jpg" alt="">
    
        <p>Pon las instrucciones o los procedimientos y los materiales de consulta en el cuerpo del manual. En la mayoría de los casos, los procedimientos y los materiales de consulta tienen sus propias secciones, aunque dentro de una sección puedes indicarle al usuario que consulte un contenido específico de la otra. De este modo, el usuario puede encontrar más rápidamente la información que esté buscando.</p>
        <li>
            <ul>
                Los procedimientos deben escribirse con una estructura consistente a lo largo de la sección de instrucciones del manual. Comienza con un resumen de la tarea, luego describe qué debe hacer el usuario y qué resultados debe observar. Los pasos deben ser numerados y comenzar con acciones verbales del mismo modo que están escritos los pasos en cada sección de este artículo.
            </ul>
        </li>
        <li>
            <ul>
                Los materiales de consulta pueden incluir una lista de opciones, consejos para la resolución de problemas y las preguntas más frecuentes. Los glosarios y los índices pueden añadirse cerca del final del manual, aunque se puede colocar una lista de términos utilizados frecuentemente al principio. Puede omitirse el índice si el manual tiene menos de 20 páginas.
            </ul>
        </li>
        <img src="imagenes/i8.jpg" alt="">
        <h1>Quinto paso</h1>
    <p>Usa imágenes gráficas para respaldar al texto cuando sea necesario. Las imágenes gráficas o las capturas de pantalla pueden ilustrar algunos puntos en el manual mejor que un texto, en especial si se trata de procedimientos complejos en los cuales el usuario necesite una confirmación visual de que esté llevando a cabo los pasos correctamente. Las imágenes gráficas pueden crearse utilizando programas de diseño asistido por computadora (CAD, por sus siglas en inglés) o programas de diseño gráfico, cámaras digitales y programas de edición de fotografías o, en el caso de las capturas de pantalla, puedes utilizar la función de captura que viene instalada en la computadora, o un programa de manejo de gráficos con una función de captura de pantallas.</p>
    <li>
        <ul>
            Una vez que hayas creado una imagen gráfica, guárdala en un formato tan comprimido como tu procesador de textos o tu programa de autoedición te permita utilizar. También debes reducir el tamaño físico de la imagen hasta alcanzar uno que pueda encajar más fácilmente en la página proveyendo simultáneamente el detalle necesario para el usuario (de ser necesario, quizás debas separar la imagen en partes para mostrar las partes relevantes junto al texto que respaldan).
        </ul>
    </li>
    <li>
        <ul>
            Si utilizas muchas imágenes gráficas en los procedimientos, hazlas de un tamaño consistente, ya sea en las mismas medidas de ancho y alto o en la misma proporción de reducción con respecto al tamaño original. Esto hará que las imágenes luzcan más atractivas para el usuario. Del mismo modo, cuando tomes capturas de pantalla en una computadora, asegúrate de que la computadora muestre la misma combinación de colores cuando captures imágenes en caso de que el manual de usuario vaya a mostrarlas en colores.
        </ul>
    </li>
    <li>
        <ul>
            Aunque los programas de diseño gráfico como Photoshop o Paint Shop Pro ofrecen funciones de captura de pantalla decentes, los programas de captura especializados como el Snagit también permiten modificar, catalogar y comentar fácilmente las capturas de pantalla.
        </ul>
    </li>
    <h1>Diseñar un manual de usuario legible</h1>
    <img src="imagenes/i9.jpg" alt="">
    <p>
        Elige algunas fuentes legibles. Aunque las computadoras permiten utilizar muchas fuentes diferentes, el objetivo de un manual de usuario es ser fácilmente legible. Para lograrlo, lo mejor es elegir un número reducido de fuentes que se vean bien juntas. Las fuentes pueden dividirse en dos grupos: las fuentes serif y las fuentes sans serif</p>    
    <li>
        <ul>
            Las fuentes serif tienen pequeñas líneas de adorno al final de los trazos principales que forman la letra. Algunas fuentes serif son Times New Roman, Baskerville, y Book Antiqua. Las fuentes serif funcionan mejor en grandes fragmentos de texto impresos en el cuerpo principal del manual de usuario en un tamaño de 10 o 12 puntos.
        </ul>
    </li>
    <li>
        <ul>
            Las fuentes sans serif solo tienen trazos que forman las letras, sin adornos. Algunas fuentes sans serif son Arial, Calibri y Century Gothic. Las fuentes sans serif se pueden utilizar en grandes fragmentos de texto en tamaños de 8 a 10 puntos en un PDF o en un manual en Internet, aunque la falta de serifs hace que las oraciones escritas en tamaños de 12 puntos o más sean difíciles de leer. Sin embargo, se pueden utilizar de manera efectiva en tamaños más grandes para los títulos y encabezados, y además son muy buenas en tamaños más pequeños para pies de página y números en columnas y tablas.
        </ul>
    </li>
    <li>
        <ul>
            Por lo general, debes elegir fuentes simples como Arial o Times New Roman para tu manual de usuario, aunque puedes elegir una fuente decorativa para las citas apartadas o los títulos si escribes un manual de usuario para un videojuego con una temática fantástica o de ciencia ficción (en el caso de las citas al margen, a menudo puedes utilizar alguna de las fuentes sencillas y mostrar la cita apartada en cursiva).
        </ul>
    </li>
    <li>
        <ul>
            Una vez que hayas elegido las fuentes que vayas a utilizar, crea una página de muestra para verificar que las fuentes se vean bien juntas en el papel. También deberías mostrarle esta muestra a la persona que deba aprobar la apariencia del manual antes de continuar escribiéndolo.
        </ul>
    </li>
